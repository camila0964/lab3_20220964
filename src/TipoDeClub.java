public class TipoDeClub {
    private boolean esDeportivo;
    private boolean esCultural;
    private boolean esAcademico;


    public boolean isEsDesportivo() {
        return esDeportivo;
    }

    public void setEsDesportivo(boolean esDesportivo) {
        this.esDeportivo = esDesportivo;
    }

    public boolean isEsCultural() {
        return esCultural;
    }

    public void setEsCultural(boolean esCultural) {
        this.esCultural = esCultural;
    }

    public boolean isEsAcademico() {
        return esAcademico;
    }

    public void setEsAcademico(boolean esAcademico) {
        this.esAcademico = esAcademico;
    }
}
